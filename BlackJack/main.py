import random
from art import logo


def deal_card():
    #Return a random card from the deck
    cards = [11, 2, 3, 4, 5, 6, 7, 8, 9, 10, 10, 10, 10]
    card = random.choice(cards)
    return card


def calculate_score(cards):
    if sum(cards) == 21 and len(cards) == 2:
        return 0

    if 11 in cards and sum(cards) == 21:  # When ace should be 1 instead of 11
        cards.remove(11)
        cards.append(1) 

    return sum(cards)

def compare(u_score, c_score):
    if u_score == c_score:
        return "Draw"
    elif c_score == 0:
        return "You Lose, Opponent has BlackJack!"
    elif u_score == 0:
        return "You Win, You have a BlackJack!"
    elif u_score > 21:
        return "You Lose, You have more than 21. You cannot have more than 21"
    elif c_score > 21:
        return "You Win, Computer has more than 21. It cannot have more than 21"
    elif u_score > c_score:
        return "You Win, You have more than computer"
    else:
        return 'You lose, Computer has more than you'

def play_again():
    print(logo)
    user_cards = []
    computer_cards = []
    computer_score = -1
    user_score = -1
    is_game_over = False

    for _ in range(2):
        user_cards.append(deal_card()) #to get dealt cards for user
        computer_cards.append(deal_card()) #to get dealt cards for comp

    while not is_game_over:
        user_score = calculate_score(user_cards)
        computer_score = calculate_score(computer_cards)
        print(f"Your cards: {user_cards}, current score: {user_score}")
        print(f"Computer's first card: {computer_cards[0]}")
        if user_score == 0 or computer_score == 0 or user_score > 21:
            is_game_over = True
        else:
            user_should_deal = input("Type 'y' to get another card, type 'n' to pass: ")
            if user_should_deal == "y":
                user_cards.append(deal_card())
            else:
                is_game_over = True

    while computer_score != 0 and computer_score < 17:
        computer_cards.append(deal_card())
        computer_score = calculate_score(computer_cards)

    print(f"Your final hand is {user_score} and final score is {user_score}")
    print(f"Computer's final hand is {computer_score} and their final score is {computer_score}")
    print(compare(user_score, computer_score))

while input("Do you want to play it again?. Type 'y' or 'n'") == 'y':
    print("\n" * 20)
    play_again()
